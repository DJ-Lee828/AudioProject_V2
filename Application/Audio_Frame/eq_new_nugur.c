#include "eq.h"
#include "adc.h"

#include <math.h>
#include <string.h>

#define FS          48000.0f
#define PI          3.14159265358979f

#define LOW_FREQ    200.0f
#define MID_FREQ    1000.0f
#define HIGH_FREQ   4000.0f

#define MID_Q       1.0f

/*
====================================================================================================
 * Biquad 구조체
====================================================================================================
*/
typedef struct
{
    float b0, b1, b2;
    float a1, a2;

    float z1, z2;

} Biquad;

/*
====================================================================================================
 * Stereo Filter
====================================================================================================
*/
static Biquad lowL, lowR;
static Biquad midL, midR;
static Biquad highL, highR;

/*
====================================================================================================
 * Gain smoothing
====================================================================================================
*/
static float s_lowDB  = 0.0f;
static float s_midDB  = 0.0f;
static float s_highDB = 0.0f;

/*
====================================================================================================
 * 이전 ADC 값 저장
====================================================================================================
*/
static uint8_t s_prevADC[3] = {255,255,255};

/*
====================================================================================================
 * Direct Form II
====================================================================================================
*/
static inline float Biquad_Process(Biquad *f, float x)
{
    float y = f->b0 * x + f->z1;

    f->z1 = f->b1 * x - f->a1 * y + f->z2;
    f->z2 = f->b2 * x - f->a2 * y;

    return y;
}

/*
====================================================================================================
 * Low Shelf
====================================================================================================
*/
static void Biquad_SetLowShelf(Biquad *f, float fc, float gainDB)
{
    float A = powf(10.0f, gainDB / 40.0f);

    float w0 = 2.0f * PI * fc / FS;

    float cosw = cosf(w0);
    float sinw = sinf(w0);

    float alpha = sinw * 0.7071f;
    float beta  = 2.0f * sqrtf(A) * alpha;

    float b0 = A * ((A + 1.0f) - (A - 1.0f) * cosw + beta);
    float b1 = 2.0f * A * ((A - 1.0f) - (A + 1.0f) * cosw);
    float b2 = A * ((A + 1.0f) - (A - 1.0f) * cosw - beta);

    float a0 = (A + 1.0f) + (A - 1.0f) * cosw + beta;
    float a1 = -2.0f * ((A - 1.0f) + (A + 1.0f) * cosw);
    float a2 = (A + 1.0f) + (A - 1.0f) * cosw - beta;

    f->b0 = b0 / a0;
    f->b1 = b1 / a0;
    f->b2 = b2 / a0;

    f->a1 = a1 / a0;
    f->a2 = a2 / a0;
}

/*
====================================================================================================
 * Mid Peaking EQ
====================================================================================================
*/
static void Biquad_SetPeaking(Biquad *f,
                              float fc,
                              float Q,
                              float gainDB)
{
    float A = powf(10.0f, gainDB / 40.0f);

    float w0 = 2.0f * PI * fc / FS;

    float cosw = cosf(w0);
    float sinw = sinf(w0);

    float alpha = sinw / (2.0f * Q);

    float b0 = 1.0f + alpha * A;
    float b1 = -2.0f * cosw;
    float b2 = 1.0f - alpha * A;

    float a0 = 1.0f + alpha / A;
    float a1 = -2.0f * cosw;
    float a2 = 1.0f - alpha / A;

    f->b0 = b0 / a0;
    f->b1 = b1 / a0;
    f->b2 = b2 / a0;

    f->a1 = a1 / a0;
    f->a2 = a2 / a0;
}

/*
====================================================================================================
 * High Shelf
====================================================================================================
*/
static void Biquad_SetHighShelf(Biquad *f,
                                float fc,
                                float gainDB)
{
    float A = powf(10.0f, gainDB / 40.0f);

    float w0 = 2.0f * PI * fc / FS;

    float cosw = cosf(w0);
    float sinw = sinf(w0);

    float alpha = sinw * 0.7071f;
    float beta  = 2.0f * sqrtf(A) * alpha;

    float b0 = A * ((A + 1.0f) + (A - 1.0f) * cosw + beta);
    float b1 = -2.0f * A * ((A - 1.0f) + (A + 1.0f) * cosw);
    float b2 = A * ((A + 1.0f) + (A - 1.0f) * cosw - beta);

    float a0 = (A + 1.0f) - (A - 1.0f) * cosw + beta;
    float a1 = 2.0f * ((A - 1.0f) - (A + 1.0f) * cosw);
    float a2 = (A + 1.0f) - (A - 1.0f) * cosw - beta;

    f->b0 = b0 / a0;
    f->b1 = b1 / a0;
    f->b2 = b2 / a0;

    f->a1 = a1 / a0;
    f->a2 = a2 / a0;
}

/*
====================================================================================================
 * Filter coefficient 갱신
 *
 * 중요:
 * z1/z2 절대 초기화하지 않는다.
====================================================================================================
*/
static void EQ_UpdateCoefficients(float lowDB,
                                  float midDB,
                                  float highDB)
{
    Biquad_SetLowShelf(&lowL, LOW_FREQ, lowDB);
    Biquad_SetLowShelf(&lowR, LOW_FREQ, lowDB);

    Biquad_SetPeaking(&midL, MID_FREQ, MID_Q, midDB);
    Biquad_SetPeaking(&midR, MID_FREQ, MID_Q, midDB);

    Biquad_SetHighShelf(&highL, HIGH_FREQ, highDB);
    Biquad_SetHighShelf(&highR, HIGH_FREQ, highDB);
}

/*
====================================================================================================
 * EQ 초기화
====================================================================================================
*/
void EQ_Init(void)
{
    memset(&lowL,  0, sizeof(Biquad));
    memset(&lowR,  0, sizeof(Biquad));

    memset(&midL,  0, sizeof(Biquad));
    memset(&midR,  0, sizeof(Biquad));

    memset(&highL, 0, sizeof(Biquad));
    memset(&highR, 0, sizeof(Biquad));

    EQ_UpdateCoefficients(0.0f, 0.0f, 0.0f);
}

/*
====================================================================================================
 * Soft clip
====================================================================================================
*/
static inline float SoftClip(float x)
{
    return tanhf(x / 32768.0f) * 32767.0f;
}

/*
====================================================================================================
 * EQ 처리
====================================================================================================
*/
void EQ_ProcessStereo(const int16_t *in,
                      int16_t *out,
                      uint32_t frameCount)
{
    /*
    ================================================================================================
     * ADC 읽기
    ================================================================================================
    */
    uint8_t adc[3];

    ADC_GetValue_EQ(adc);

    /*
    ================================================================================================
     * ADC 변경 시만 coefficient 갱신
    ================================================================================================
    */
    if( adc[0] != s_prevADC[0] ||
        adc[1] != s_prevADC[1] ||
        adc[2] != s_prevADC[2] )
    {
        s_prevADC[0] = adc[0];
        s_prevADC[1] = adc[1];
        s_prevADC[2] = adc[2];

        /*
        --------------------------------------------------------------------------------------------
         * Normalize
        --------------------------------------------------------------------------------------------
        */
        float nLow  = ((float)adc[0] - 50.0f) / 50.0f;
        float nMid  = ((float)adc[1] - 50.0f) / 50.0f;
        float nHigh = ((float)adc[2] - 50.0f) / 50.0f;

        /*
        --------------------------------------------------------------------------------------------
         * Cubic curve
        --------------------------------------------------------------------------------------------
        */
        nLow  = nLow  * nLow  * nLow;
        nMid  = nMid  * nMid  * nMid;
        nHigh = nHigh * nHigh * nHigh;

        /*
        --------------------------------------------------------------------------------------------
         * dB
        --------------------------------------------------------------------------------------------
        */
        float targetLowDB  = nLow  * 15.0f;
        float targetMidDB  = nMid  * 15.0f;
        float targetHighDB = nHigh * 12.0f;

        /*
        --------------------------------------------------------------------------------------------
         * smoothing
        --------------------------------------------------------------------------------------------
        */
        s_lowDB  += 0.15f * (targetLowDB  - s_lowDB);
        s_midDB  += 0.15f * (targetMidDB  - s_midDB);
        s_highDB += 0.15f * (targetHighDB - s_highDB);

        /*
        --------------------------------------------------------------------------------------------
         * coefficient update
        --------------------------------------------------------------------------------------------
        */
        EQ_UpdateCoefficients(s_lowDB,
                              s_midDB,
                              s_highDB);
    }

    /*
    ================================================================================================
     * Audio processing
    ================================================================================================
    */
    for(uint32_t i=0; i<frameCount; i++)
    {
        float xL = (float)in[2*i];
        float xR = (float)in[2*i + 1];

        /*
        --------------------------------------------------------------------------------------------
         * Serial EQ
        --------------------------------------------------------------------------------------------
        */
        xL = Biquad_Process(&lowL, xL);
        xL = Biquad_Process(&midL, xL);
        xL = Biquad_Process(&highL, xL);

        xR = Biquad_Process(&lowR, xR);
        xR = Biquad_Process(&midR, xR);
        xR = Biquad_Process(&highR, xR);

        /*
        --------------------------------------------------------------------------------------------
         * Soft clipping
        --------------------------------------------------------------------------------------------
        */
        xL = SoftClip(xL);
        xR = SoftClip(xR);

        /*
        --------------------------------------------------------------------------------------------
         * Output
        --------------------------------------------------------------------------------------------
        */
        out[2*i]     = (int16_t)xL;
        out[2*i + 1] = (int16_t)xR;
    }
}
