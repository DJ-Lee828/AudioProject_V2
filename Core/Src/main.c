static void Log_WaitBoot(void)
{
	ssd1306_Init(&hi2c2);

	ssd1306_SetCursor(0, 16);
	ssd1306_WriteString("BOOT", Font_16x26, White);
	ssd1306_UpdateScreen(&hi2c2);

	HAL_Delay(1000);

	for (int i = 0; i < 3; i++)
	{
		ssd1306_WriteString(".", Font_16x26, White);
		ssd1306_UpdateScreen(&hi2c2);
		HAL_Delay(1000);
	}
}

